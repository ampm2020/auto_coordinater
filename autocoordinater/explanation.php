<h1>クローゼットについて</h1>
<div>
    <p>・服はあらかじめデータベースに登録しておく必要があります。クローゼットのアイコンを押すと管理ページに移動します。</p>
    <p>・クローゼットでは、衣服の追加／検索／削除ができます。<br>
【登録】「新しい服を追加する」をクリックし、服の写真と分類を選択してから「追加」ボタンを押してください。成功するとメッセージが表示されます。<br>
【検索】表示したい種類の服にチェックを入れて「表示」を押してください。また、画像をクリックすると拡大できます。<br>
【削除】拡大画面で左上の「削除」ボタンを押してください。</p>
</div>
