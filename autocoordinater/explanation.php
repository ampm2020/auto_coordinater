    <p>・人間の代わりにその日に着る服を選んでくれるアプリです。服選びに頭を使いたくなかったため作りました。</p>
    <p>・最高気温と最低気温を入力して「コーディネート」ボタンを押すと、データベースに登録した服から適当なものが表示されます。</p>
    <p>・服の分類、および選定基準はあくまで作成者自身の都合に合わせたものです。ご了承ください。</p>
    <p>・「これで決まり！」を押すと、翌々日まではそれらの服が選択候補に現れなくなります。(洗濯が終わるまでのインターバルを考慮しています。アウターなどは除く)</p>
    <p>・条件に合う服が１着もない場合は「no image」の画像が表示されます。そんな時は新しい服を買いに行くのがよいでしょう。</p>
    <p>・「クローゼット」画面で登録済みの服を検索することもできます。今どんな服を持っているのかがすぐわかるので便利です。</p>
    <p>・下着や靴下などは考慮していません。</p>
</div>
